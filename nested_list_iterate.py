class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        if len(nestedList) == 0: return 0
        
        stk = []
        tot = 0
        
        for cur in nestedList:
            stk.append((cur, 1))
            
        while stk:
            nxt, depth = stk.pop(0)
            if nxt.isInteger(): tot += depth * nxt.getInteger()
            else:
                for nxt_int in nxt.getList():
                    stk.append((nxt_int,depth+1))
                    
        return tot
        
